    private void processBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = adulteratedMedicineTable.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
        
        request = (ComplainWorkRequest) adulteratedMedicineTable.getValueAt(selectedRow, 0);
        request.setStatus("Processing");
        
        ProcessMedicineFraudJPanel processAdulterationFraudJPanel = new ProcessMedicineFraudJPanel(userProcessContainer,request);
        userProcessContainer.add("ProcessMedicineFraudComplainJPanel",processAdulterationFraudJPanel);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_processBtnActionPerformed

