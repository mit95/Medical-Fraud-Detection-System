    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel) FraudComplainTable.getModel();
        
        model.setRowCount(0);       
        for(DepartmentToLawWorkRequest request:lawEnforcementOrganization.getWorkQueue().getDepartmenttolawWorkRequestList()){
            Object[]row = new Object[5];
            row[0] = request;
            row[1] = request.getMessage();
            row[2] = request.getSender();//getReq().getSender().getEmployee().getName();
            row[3] = request.getReceiver();//.getReq().getReceiver().getEmployee().getName();
            row[4] = request.getStatus();//getReq().getStatus();
            
            model.addRow(row);
        }
    }
    
