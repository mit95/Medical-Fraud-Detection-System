    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = FraudComplainTable.getSelectedRow();

        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a row");
        }

        DepartmentToLawWorkRequest request = (DepartmentToLawWorkRequest)FraudComplainTable.getValueAt(selectedRow, 0);
        useraccount.getWorkQueue().getDepartmenttolawWorkRequestList().add(request);
        
        request.setReceiver(useraccount);
        //request.setReceiver(userAccount);
        //request.setStatus("Pending");
        populateTable();
    }//GEN-LAST:event_assignBtnActionPerformed

    private void processBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processBtnActionPerformed
        // TODO add your handling code here:

        int selectedRow = FraudComplainTable.getSelectedRow();
        
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a row");
        }
        else{
            DepartmentToLawWorkRequest request = (DepartmentToLawWorkRequest) FraudComplainTable.getValueAt(selectedRow, 0);
            if(request.getReceiver()==null){
                JOptionPane.showMessageDialog(null, "Work request not assigned to anyone");
            }
            else{
                if(request.getStatus().equals("Completed")){
                    JOptionPane.showMessageDialog(null, "Request already processed");
                }
                else{
                request.setStatus("Processing");
                request.setOfficerName(useraccount.getUsername());
                ProcessFraudComplainJPanel processFraudComplainJPanel = new ProcessFraudComplainJPanel(userProcessContainer,request,business);
                userProcessContainer.add("ProcessFraudComplainJPanel",processFraudComplainJPanel);
                CardLayout layout = (CardLayout)userProcessContainer.getLayout();
                layout.next(userProcessContainer);
                }
            }   
        }
    }//GEN-LAST:event_processBtnActionPerformed


